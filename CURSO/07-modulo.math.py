import math #modulo para hacer cuentas matematicas


print(round(1.3)) #round devuelve el numero entero que se encuentre mas cercano a ese decimal
print(abs(-77)) #devuelve el valor absoluto del numero , le saca los signos

print(math.ceil(1.1)) #devuelve el numero entero mas cercano hacia arriba
print(math.floor(1.3)) #devuelve el numero entero mas cercano hacia abajo
print(math.isnan(0.3)) #comprueba si el valor que pusimos NO es un numero
print(math.pow(10, 3)) # eleva un numero por el valor que pongamos a la izquierda
print(math.sqrt(25)) #saca la raiz cuadrada
